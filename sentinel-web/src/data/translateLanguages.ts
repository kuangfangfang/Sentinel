export function findTranslateLanguage(languageCode: string) {
  return TRANSLATE_LANGUAGES.find((language) => language.value === languageCode) ?? null;
}

export function searchTranslateLanguages(query: string) {
  const normalisedQuery = query.trim().toLowerCase();
  if (!normalisedQuery) return TRANSLATE_LANGUAGES;

  return TRANSLATE_LANGUAGES.filter((language) => {
    const haystack = `${language.label} ${language.value} ${language.searchText ?? ''}`.toLowerCase();
    return haystack.includes(normalisedQuery);
  });
}
